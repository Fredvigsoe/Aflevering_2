public class Employee {
    String name;
    int salary;
    String emailAsUsername;
    String passWord;
    String jobDescription;

    public Employee(String name, int salary, String emailAsUsername, String passWord, String jobDescription){
        this.name = name;
        this.salary = salary;
        this.emailAsUsername = emailAsUsername;
        this.passWord = passWord;
        this.jobDescription = jobDescription;
    }


}
